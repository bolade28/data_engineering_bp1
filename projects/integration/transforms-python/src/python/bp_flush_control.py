from datetime import date

# Flushing constants
#
# FLUSH IS CURRENTLY ENABLED IN ALL PIPELINES UNTIL FURTHER NOTICE
# DO NOT MODIFY THIS FILE
#
# To run a flush of a pipeline:
#
#   - set the relevant XXX_FLUSH constant to True
#   - increment the relevant XXX_VERSION_NUMBER (so if it was 1234, set to 1235)
#   - commit and wait for checks to run
#   - build the pipeline
#   - set the relevant XXX_FLUSH constant to False
#   - leave the XXX_VERSION_NUMBER at its new value (in the above example, 1235)
#   - commit
#
# Note: don't use YYYYMMDD or anything like that for the version number
# as it doesn't allow for multiple flushes per day.  Keep it sequential.
#
# Here I have set the base to 10^7 to avoid collisions with previously-used
# date-based version numbers.


